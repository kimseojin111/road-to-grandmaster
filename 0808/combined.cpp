using S = int; 

S op(S l, S r){
    return max(l,r); 
}

S e(){
    return 0; 
}

using F = int; 

S mapping(F a, S s){
    return s+a; 
}

// l(r(x)) 
F composition(F l, F r){
    return l+r; 
}

F id() {
    return 0; 
}

