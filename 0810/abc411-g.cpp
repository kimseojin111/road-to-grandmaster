mint adj[22][22]; 
mint dp[1<<21][22]; 

void solve(){
    int n,m; cin>>n>>m; 
    rep(i,m){
        int a,b; cin>>a>>b; 
        a--; b--; 
        adj[a][b]++; adj[b][a]++; 
    }
    mint ans = 0; 
    for(int i=1;i<n;i++){
        // i가 최대 
        for(int j=0;j<(1<<i);j++) for(int k=0;k<i;k++) dp[j][k] = 0; 
        for(int j=0;j<i;j++) dp[1<<j][j] = adj[i][j]; 
        for(int bit=1;bit<(1<<i);bit++) for(int j=0;j<i;j++){
            if(__builtin_popcount(bit)>=2) ans += dp[bit][j]*adj[i][j]/2; 
            for(int k=0;k<i;k++) if(!((bit>>k)&1)) dp[bit|(1<<k)][k] += dp[bit][j]*adj[j][k]; 
        }
        for(int j=0;j<i;j++) ans += (adj[i][j])*(adj[i][j]-1)/2; 
    }
    cout << ans.val(); 
}

