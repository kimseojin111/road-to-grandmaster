const int N = 3000000; 
int cnt[N];
int pen[N];  
void upd(int v, int x){
    while(v<N){
        pen[v]+=x; v+=v&-v; 
    }
}
int qry(int v){
    int re = 0; 
    while(v){
        re += pen[v]; v-=v&-v; 
    }
    return re; 
}

void solve(){
    int q; cin>>q; 
    for(int i=1;i<N;i++) {
        cnt[i]=1; upd(i,1); 
    }
    while(q--){
        int a,b; cin>>a>>b; 
        if(a<N && cnt[a]==1){
            for(int i=a;i<N;i+=a) if(cnt[i]){
                upd(i,-1); cnt[i]=0; 
            }
        }
        int l=1,r=N-1; 
        if(qry(1)>=b){
            cout << 1 << "\n"; 
        }
        else {
            while(l+1<r){
                int m = l+r>>1; 
                if(qry(m)>=b) r=m; 
                else l=m; 
            }
            cout << r << "\n"; 
        }
    }
}

signed main() {
   cin.tie(0)->ios::sync_with_stdio(0);
   cout.tie(nullptr);
   int t = 1;
   //cin >> t;
   while(t--) solve(); 
   return 0;
}